-- =====================================================
-- Стандартні схеми залів (6 рядів × 8 місць)
-- =====================================================

INSERT INTO public.seats (id, event_id, "row", number, price)
SELECT
    uuid_generate_v4(),
    e.id,
    r.r,
    n.n,
    CASE
        WHEN r.r <= 2 THEN 500
        ELSE 300
    END
FROM public.events e
CROSS JOIN generate_series(1, 6) r(r)
CROSS JOIN generate_series(1, 8) n(n)
WHERE e.id NOT IN (
    '11111111-1111-1111-1111-111111111111',
    '33333333-3333-3333-3333-333333333333',
    '77777777-7777-7777-7777-777777777777',
    'aaaaaaaa-aaaa-aaaa-aaaa-aaaaaaaaaaaa'
);

-- =====================================================
-- Театральна схема (трапеція)
-- Джаз, Лісова пісня, Стендап
-- =====================================================

INSERT INTO public.seats (id, event_id, "row", number, price)
SELECT
    uuid_generate_v4(),
    e.id,
    r.row_num,
    n.num,
    CASE
        WHEN r.row_num <= 2 THEN 600
        ELSE 400
    END
FROM public.events e
JOIN (
    VALUES
        (1, 4),
        (2, 5),
        (3, 6),
        (4, 7),
        (5, 8),
        (6, 8)
) r(row_num, max_seats) ON TRUE
JOIN LATERAL generate_series(1, r.max_seats) n(num) ON TRUE
WHERE e.id IN (
    '11111111-1111-1111-1111-111111111111',
    '33333333-3333-3333-3333-333333333333',
    'aaaaaaaa-aaaa-aaaa-aaaa-aaaaaaaaaaaa'
);

